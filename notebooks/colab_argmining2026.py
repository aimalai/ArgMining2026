# End-to-End Resolution Processing Pipeline
# Iterates through document paragraphs to execute v2 classification and relation extraction, aggregating structural metadata and argumentative links into the final submission-ready JSON format.

def process_full_resolution_v2(file_path, model, tokenizer):
    with open(file_path, 'r', encoding='utf-8') as f:
        data = json.load(f)

    raw_paras = data if isinstance(data, list) else data.get('body', {}).get('paras', [])
    submission_paras = []
    preambular_indices = []
    operative_indices = []

    print(f"📄 Processing {len(raw_paras)} paragraphs with v2 Logic...")

    for i, p in enumerate(raw_paras):
        text = p.get('text_en', '')

        # 1. Use the NEW v2 Classifier
        s1_raw = subtask1_classifier_v2(text, model, tokenizer)
        try:
            s1_data = json.loads(s1_raw)
        except:
            s1_data = {"type": "header", "think": "Error parsing JSON."}

        # 2. Map types for the METADATA block
        # We only track non-headers for the competition indices
        if s1_data['type'] == 'operative':
            operative_indices.append(i)
        elif s1_data['type'] == 'preambular':
            preambular_indices.append(i)

        # 3. Subtask 2: Relations (Only if NOT a header)
        matched_paras = {}
        if i > 0 and s1_data['type'] != 'header':
            parent_text = raw_paras[i-1].get('text_en', '')
            s2_raw = predict_argumentative_relation(text, parent_text, model, tokenizer)
            try:
                s2_data = json.loads(s2_raw)
                if s2_data.get('relation_type') != 'none':
                    matched_paras[str(i-1)] = s2_data['relation_type']
            except:
                pass

        submission_paras.append({
            "type": s1_data['type'] if s1_data['type'] != 'header' else 'preambular', # Map headers to preambular for competition compliance
            "tags": [],
            "matched_paras": matched_paras,
            "think": s1_data.get('think', "")
        })

    return {
        "METADATA": {
            "preambular_paras": preambular_indices,
            "operative_paras": operative_indices,
            "think": "Structural analysis complete."
        },
        "paras": submission_paras
    }

