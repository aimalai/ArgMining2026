# Pairwise Argumentative Relation Extraction
# Determines the rhetorical relationship (supporting, contradictive, complemental, modifying) between a target paragraph and a context paragraph using a pairwise comparison prompt.

def predict_argumentative_relation(current_text, parent_text, model, tokenizer):
    """
    Subtask 2: Identify relations between two paragraphs.
    Types: supporting, contradictive, complemental, modifying.
    """
    prompt = f"""<|begin_of_text|><|start_header_id|>system<|end_header_id|>
Compare the 'Current' paragraph to the 'Parent' paragraph.
Does the Current paragraph support, contradict, complement, or modify the Parent?

Return valid JSON ONLY.
<|eot_id|><|start_header_id|>user<|end_header_id|>
Parent: "{parent_text}"
Current: "{current_text}"

Target JSON:
{{
  "relation_type": "supporting/contradictive/complemental/modifying/none",
  "think": "Explain the argumentative link."
}}
<|eot_id|><|start_header_id|>assistant<|end_header_id|>
"""
    inputs = tokenizer(prompt, return_tensors="pt").to(model.device)
    with torch.no_grad():
        output_tokens = model.generate(**inputs, max_new_tokens=150, temperature=0.1)

    return tokenizer.decode(output_tokens[0][inputs['input_ids'].shape[-1]:], skip_special_tokens=True)

